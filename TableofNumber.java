import java.util.Scanner;

public class TableofNumber {
	
	public static void main(String []args)
	{
		int i = 0;
		int y = 0;
		
		System.out.println("");	
		
		Scanner sc= new Scanner(System.in);
		System.out.print("Enter the Number for which table needs to be prints : ");	
		i = sc.nextInt();
		
		
		System.out.println("");	
		for(int z = 0; z<10; z++ )
		{
			
			y++;
			System.out.println(i+" * "+y+ " = "+(i*y));	
		}
		System.out.println("");	
		
	}
}
